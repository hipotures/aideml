def preprocess(df: pd.DataFrame) -> pd.DataFrame:
    import numpy as np
    import pandas as pd

    out = df.copy()

    out["Race_norm"] = (
        out["Race"].astype("string").str.strip().str.upper().astype("category")
    )
    out["Driver_norm"] = (
        out["Driver"].astype("string").str.strip().str.upper().astype("category")
    )
    out["Compound_norm"] = (
        out["Compound"].astype("string").str.strip().str.upper().astype("category")
    )

    comp = out["Compound"].astype("string").str.strip().str.upper()
    out["is_intermediate"] = (comp == "INTERMEDIATE").astype("int8")
    out["is_full_wet"] = (comp == "WET").astype("int8")
    out["is_wet_tyre"] = comp.isin(["INTERMEDIATE", "WET"]).astype("int8")
    out["is_slick_tyre"] = comp.isin(["SOFT", "MEDIUM", "HARD"]).astype("int8")

    for c in ["Race_norm", "Driver_norm", "Compound_norm"]:
        freq = out[c].value_counts(normalize=True)
        out[c + "_freq"] = out[c].map(freq).astype("float32")

    g = ["Year", "Race_norm", "LapNumber"]
    rg = ["Year", "Race_norm"]
    lap_time = "LapTime (s)"

    grp = out.groupby(g, observed=True, sort=False)
    out["field_size"] = grp["Driver_norm"].transform("count").astype("int16")
    out["race_lap_pit_count"] = grp["PitStop"].transform("sum").astype("float32")
    out["race_lap_pit_rate"] = (
        out["race_lap_pit_count"] / out["field_size"].clip(lower=1)
    ).astype("float32")
    out["race_lap_wet_share"] = grp["is_wet_tyre"].transform("mean").astype("float32")
    out["race_lap_median_laptime"] = grp[lap_time].transform("median").astype("float32")
    out["race_lap_laptime_std"] = (
        grp[lap_time].transform("std").fillna(0).astype("float32")
    )
    out["field_laptime_delta_to_median"] = (
        out[lap_time] - out["race_lap_median_laptime"]
    ).astype("float32")
    out["field_laptime_z"] = (
        (
            out["field_laptime_delta_to_median"]
            / out["race_lap_laptime_std"].replace(0, np.nan)
        )
        .replace([np.inf, -np.inf], np.nan)
        .astype("float32")
    )

    lap_stats = (
        out[
            g
            + [
                "race_lap_pit_count",
                "field_size",
                "race_lap_wet_share",
                "race_lap_median_laptime",
            ]
        ]
        .drop_duplicates(g)
        .sort_values(rg + ["LapNumber"])
    )

    race_grp = lap_stats.groupby(rg, observed=True, sort=False)
    lap_stats["lap_median_laptime_delta_prev"] = (
        race_grp["race_lap_median_laptime"].diff().fillna(0).astype("float32")
    )
    lap_stats["wet_share_delta_prev"] = (
        race_grp["race_lap_wet_share"].diff().fillna(0).astype("float32")
    )
    lap_stats["field_pit_wave_last_2laps"] = (
        lap_stats["race_lap_pit_count"]
        + race_grp["race_lap_pit_count"].shift(1).fillna(0)
    ).astype("float32")
    lap_stats["field_pit_wave_prev_2laps"] = (
        race_grp["race_lap_pit_count"].shift(1).fillna(0)
        + race_grp["race_lap_pit_count"].shift(2).fillna(0)
    ).astype("float32")

    race_median_time = race_grp["race_lap_median_laptime"].transform("median")
    lap_stats["safety_car_or_vsc_proxy"] = (
        (lap_stats["lap_median_laptime_delta_prev"] > 8.0)
        | (lap_stats["race_lap_median_laptime"] > race_median_time * 1.10)
    ).astype("int8")
    lap_stats["weather_change_proxy"] = (
        (lap_stats["wet_share_delta_prev"].abs() > 0.05)
        | (
            (lap_stats["race_lap_wet_share"] > 0)
            & (lap_stats["lap_median_laptime_delta_prev"] > 3.0)
        )
    ).astype("int8")
    lap_stats["under_vsc"] = lap_stats["safety_car_or_vsc_proxy"]
    lap_stats["rain_onset_proxy"] = (
        (lap_stats["wet_share_delta_prev"] > 0.05)
        | (
            (lap_stats["race_lap_wet_share"] > 0)
            & (race_grp["race_lap_wet_share"].shift(1).fillna(0) == 0)
        )
    ).astype("int8")

    lap_stats["race_status_proxy"] = (
        (lap_stats["safety_car_or_vsc_proxy"] == 1)
        | (lap_stats["weather_change_proxy"] == 1)
    ).astype("int8")
    prev_status = race_grp["race_status_proxy"].shift(1)
    status_change = (
        prev_status.isna() | (lap_stats["race_status_proxy"] != prev_status)
    ).astype("int16")
    lap_stats["_status_segment"] = status_change.groupby(
        [lap_stats["Year"], lap_stats["Race_norm"]], observed=True
    ).cumsum()
    lap_stats["laps_since_status_change"] = (
        lap_stats.groupby(["Year", "Race_norm", "_status_segment"], observed=True)
        .cumcount()
        .astype("int16")
    )

    out = out.merge(
        lap_stats[
            g
            + [
                "lap_median_laptime_delta_prev",
                "wet_share_delta_prev",
                "field_pit_wave_last_2laps",
                "field_pit_wave_prev_2laps",
                "safety_car_or_vsc_proxy",
                "weather_change_proxy",
                "under_vsc",
                "rain_onset_proxy",
                "laps_since_status_change",
            ]
        ],
        on=g,
        how="left",
        sort=False,
    )

    out["field_pit_wave_last_2laps_ex_self"] = (
        (out["field_pit_wave_last_2laps"] - out["PitStop"])
        .clip(lower=0)
        .astype("float32")
    )
    out["field_pit_wave_rate_last_2laps"] = (
        out["field_pit_wave_last_2laps"] / (2.0 * out["field_size"].clip(lower=1))
    ).astype("float32")
    out["status_x_pit_wave"] = (
        out["safety_car_or_vsc_proxy"] * out["field_pit_wave_rate_last_2laps"]
    ).astype("float32")
    out["weather_x_wet_share"] = (
        out["weather_change_proxy"] * out["race_lap_wet_share"]
    ).astype("float32")

    traffic = out[
        g
        + [
            "Position",
            lap_time,
            "TyreLife",
            "Stint",
            "Cumulative_Degradation",
            "Compound_norm",
        ]
    ].copy()
    traffic["_row"] = np.arange(len(out), dtype=np.int64)
    traffic = traffic.sort_values(g + ["Position", "_row"])
    tgrp = traffic.groupby(g, observed=True, sort=False)

    traffic["_ahead_laptime"] = tgrp[lap_time].shift(1)
    traffic["_ahead_tyre"] = tgrp["TyreLife"].shift(1)
    traffic["_ahead_stint"] = tgrp["Stint"].shift(1)
    traffic["_ahead_deg"] = tgrp["Cumulative_Degradation"].shift(1)
    traffic["_ahead_compound"] = tgrp["Compound_norm"].shift(1)
    traffic["_behind_laptime"] = tgrp[lap_time].shift(-1)

    idx = traffic["_row"].to_numpy()
    for name, values in {
        "gap_ahead_laptime_delta": traffic[lap_time] - traffic["_ahead_laptime"],
        "gap_behind_laptime_delta": traffic["_behind_laptime"] - traffic[lap_time],
        "tyrelife_delta_to_car_ahead": traffic["TyreLife"] - traffic["_ahead_tyre"],
        "degradation_delta_to_car_ahead": traffic["Cumulative_Degradation"]
        - traffic["_ahead_deg"],
    }.items():
        arr = np.full(len(out), np.nan, dtype=np.float32)
        arr[idx] = pd.to_numeric(values, errors="coerce").astype("float32").to_numpy()
        out[name] = arr

    small_gap = (
        traffic["_ahead_laptime"].notna()
        & (traffic["Position"] > 1)
        & ((traffic[lap_time] - traffic["_ahead_laptime"]) <= 1.5)
        & ((traffic[lap_time] - traffic["_ahead_laptime"]) >= -3.0)
    )
    same_stint_ahead = (traffic["Stint"] == traffic["_ahead_stint"]).fillna(False)
    same_compound_ahead = (
        traffic["Compound_norm"].astype("object")
        == traffic["_ahead_compound"].astype("object")
    ).fillna(False)

    for name, values in {
        "gap_ahead_small_for_undercut": small_gap.astype("int8"),
        "same_stint_as_car_ahead": same_stint_ahead.astype("int8"),
        "same_compound_as_car_ahead": same_compound_ahead.astype("int8"),
    }.items():
        arr = np.zeros(len(out), dtype=np.int8)
        arr[idx] = values.to_numpy(dtype=np.int8)
        out[name] = arr

    out["undercut_pressure_score"] = (
        out["gap_ahead_small_for_undercut"]
        * np.maximum(out["tyrelife_delta_to_car_ahead"].fillna(0), 0)
        * (1.0 + out["field_pit_wave_rate_last_2laps"].fillna(0))
    ).astype("float32")
    out["traffic_status_pit_window"] = (
        out["gap_ahead_small_for_undercut"]
        * (1 + out["safety_car_or_vsc_proxy"])
        * (1 + out["weather_change_proxy"])
    ).astype("float32")

    return out


